# Atividade - Sistema de Orçamento Personalizado para Reformas Residenciais

# Você foi contratado para desenvolver um programa que faça o orçamento de uma reforma residencial. O sistema deve calcular o valor total da reforma, aplicando uma série de regras e condições de acordo com o tipo de reforma, os materiais utilizados e a área da casa.

# Tipos de Reforma:
# Reforma Simples: R$ 300,00 por metro quadrado.
# Reforma Completa: R$ 500,00 por metro quadrado.
# Reforma de Luxo: R$ 800,00 por metro quadrado.

# Materiais Utilizados:
# Padrão: Sem custo adicional.
# Premium: Acrescentar 20% no valor total da reforma.
# Luxo: Acrescentar 50% no valor total da reforma.

# Tamanho da Reforma:
# Para reformas acima de 100 metros quadrados, aplicar um desconto de 10% no valor total.
# Para reformas abaixo de 30 metros quadrados, acrescentar 15% no valor total (custo adicional por ser uma obra pequena).

# Localização:
# Reformas em regiões centrais têm um acréscimo de R$ 5.000,00 no valor total.
# Reformas em regiões periféricas têm um desconto de R$ 3.000,00 no valor total.

# Equipe de Trabalho:
# Para reformas acima de 150 metros quadrados, é necessário contratar uma equipe de trabalho. O custo da equipe é de R$ 2.000,00 por pessoa, e cada pessoa da equipe pode trabalhar em até 50 metros quadrados de obra.

# O sistema deve solicitar ao usuário as seguintes informações:

# Tipo de Reforma (Simples, Completa ou Luxo)
# Material Utilizado (Padrão, Premium ou Luxo)
# Tamanho da Reforma (em metros quadrados)
# Localização (Central ou Periférica)
# Quantidade de Pessoas na Equipe de Trabalho

# O sistema deve calcular e exibir o valor total da reforma, considerando todas as regras e condições apresentadas.